# LOGICAL OPERATORS


#  AND Operator

# Both condition is True the entire condition is True
price = 25
print( price > 10 and price <30)  # True


# OR Operator
# If one boolean condition is True then the Entire Condition will be True

price = 15
print( price > 5 or price > 25) # True

# NOT Operator
# It will inverse the True to False and False to True

price = 45
print( not price < 55) # False



 # Recap

# 1. AND - Both Condition True it will return True
# 2. OR  - One Condition is True  it will return True
# 3. NOT - Inverse the T to F and F to T



